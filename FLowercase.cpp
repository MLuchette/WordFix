//////////////////////////////////////////////////////////////////////
// IFilter's execute function definition for Lowercase Filter
//////////////////////////////////////////////////////////////////////
void FLowercase::execute(string word)
{
	for(unsigned i = 0; i< word.length(); i++)
	{
		word[i] = tolower(word[i]);
	}
	cout << word;
}